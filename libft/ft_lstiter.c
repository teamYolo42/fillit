#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *tmp;

	tmp = lst;
	(void)tmp;
	if (lst)
		while (lst)
		{
			f(lst);
			lst = lst->next;
		}
}
